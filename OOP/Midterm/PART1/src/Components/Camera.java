package Components;

public abstract class Camera {
    String spec;
    String features;

    public String getSpec() {
        return spec;
    }

    public void setSpec(String spec) {
        this.spec = spec;
    }

    public String getFeatures() {
        return features;
    }

    public void setFeatures(String size) {
        this.features = size;
    }
}
